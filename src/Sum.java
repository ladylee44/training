public class Sum {
    public static void sum() {
        int a = 5, b = 6;
        int sum;
        sum = a + b;
        System.out.println("Tong 2 so la " + sum);
    }

    public static void binhphuong() {
        int c = 6;
        int binhphuong;
        binhphuong = c * c;
        System.out.println("Binh phuong la " + binhphuong);
    }

    public static void main(String[] args) {
        sum();
        binhphuong();
    }
}
